package info.project.orion.Model;

public class Events {
    String title;
    String image;
    String price;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDate() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
